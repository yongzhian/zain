package threadlocal;

/**
 * @author Zain
 */
public class Connection {
    private int id;

    private boolean isBusy;

    public Connection(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public boolean isBusy() {
        return isBusy;
    }

    public void setBusy(boolean busy) {
        isBusy = busy;
    }
}
